import java.awt.Color;
import java.util.*;
public class Chips 
{
    private int value;
    private Color color;
    public Chips (int v, Color color1)
    {
        value = v;
        color = color1;
        
    }
    
    public int getValue()
    {
        return value;
    }
    
    public Color getColor()
    {
        return color;
    }
    
    
 
}
